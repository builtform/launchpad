def _hint_for_reason(reason: str) -> str:
    return _REASON_HINTS.get(
        reason,
        f"validation-rejection reason: {reason}; see HANDSHAKE §4 for details",
    )


def _write_part_1(reason: str, *, stderr: TextIO) -> None:
    """Part 1: BEFORE forensic-log write attempt — independent of forensic
    success."""
    print(f"reason: {reason}: {_hint_for_reason(reason)}", file=stderr)


def _write_part_2(written_path: Path | None, *, stderr: TextIO) -> None:
    """Part 2: AFTER `O_CREAT|O_EXCL` retry resolves — actual final path."""
    if written_path is not None:
        print(f"log written to: {written_path}", file=stderr)
    else:
        print("forensic log unavailable; reason captured above", file=stderr)


def _try_write_jsonl(target: Path, line: bytes) -> bool:
    """Attempt single O_CREAT|O_EXCL write of `line` to `target`.

    Returns True on success, False on FileExistsError. Re-raises other
    OSErrors so the caller's outer try/except handles ENOENT/EROFS/EACCES/
    ENOSPC fallback.
    """
    fd = os.open(str(target), os.O_WRONLY | os.O_CREAT | os.O_EXCL, 0o600)
    try:
        try:
            os.fchmod(fd, 0o600)
        except OSError:
            pass
        os.write(fd, line)
        os.fsync(fd)
        if sys.platform == "darwin":
            try:
                fcntl.fcntl(fd, fcntl.F_FULLFSYNC)
            except (OSError, AttributeError):
                pass
    finally:
        os.close(fd)
    return True


def write_rejection(
    repo_root: Path,
    *,
    reason: str,
    field_name: str | None = None,
    seen_version: str | None = None,
    extra: Mapping[str, Any] | None = None,
    stderr: TextIO | None = None,
) -> Path | None:
    """Layer 8/9 hardened inline scaffold-rejection-<ts>.jsonl write.

    Returns the actual path written on success, or None if the forensic
    log was unavailable (filesystem fallback to stderr taken).

    Two-part stderr is emitted in both cases:
      Part 1: reason + hint (BEFORE write).
      Part 2: log path or "unavailable" (AFTER write attempt resolves).
    """
    if stderr is None:
        stderr = sys.stderr

    # Part 1 — fire BEFORE forensic-log write (Layer 9: path-announce-after-retry).
    _write_part_1(reason, stderr=stderr)

    payload = _build_payload(
        reason=reason,
        field_name=field_name,
        seen_version=seen_version,
        extra=extra,
    )
    line = (json.dumps(
        payload,
        sort_keys=True,
        separators=(",", ":"),
        ensure_ascii=True,
        allow_nan=False,
    ) + "\n").encode("utf-8")
    if len(line) > MAX_LINE_BYTES:
        # Truncate would corrupt JSON; emit fallback to stderr.
        print(f"JSONL-fallback: payload exceeds {MAX_LINE_BYTES} bytes",
              file=stderr)
        print(f"JSONL-fallback: {payload}", file=stderr)
        _write_part_2(None, stderr=stderr)
        return None

    # Best-effort makedirs wrapped in try/except per Layer 8 ENOENT crash close.
    try:
        obs = _harness_obs_dir(repo_root)
        obs.mkdir(parents=True, exist_ok=True)
    except OSError as exc:
        # ENOENT / EROFS / EACCES / ENOSPC — emit JSONL-fallback to stderr.
        print(f"JSONL-fallback: makedirs failed ({exc.errno}={exc.strerror}): {payload}",
              file=stderr)
        _write_part_2(None, stderr=stderr)
        return None

    base_ts = _utc_now_microsec()
    pid = os.getpid()
    base_name = f"{REJECTION_FILENAME_PREFIX}{base_ts}.{pid}{REJECTION_FILENAME_SUFFIX}"
    written: Path | None = None

    for attempt in range(1, MAX_RETRIES + 1):
        if attempt == 1:
            target = obs / base_name
        else:
            # Retry suffix per Layer 8 simplification: .r2/.r3/...
            target = obs / (base_name + f".r{attempt}")
        try:
            _try_write_jsonl(target, line)
            written = target
            break
        except FileExistsError:
            continue
        except OSError as exc:
            # ENOENT/EROFS/EACCES/ENOSPC — fallback to stderr immediately.
            if exc.errno in (
                errno.ENOENT, errno.EROFS, errno.EACCES, errno.ENOSPC,
            ):
                print(f"JSONL-fallback: write failed ({exc.errno}={exc.strerror}): {payload}",
                      file=stderr)
                break
            # Other OSError: log fallback then surface as part 2.
            print(f"JSONL-fallback: unexpected OSError ({exc}): {payload}",
                  file=stderr)
            break

    if written is not None:
        # Best-effort directory fsync.
        try:
            dirfd = os.open(str(_harness_obs_dir(repo_root)), os.O_RDONLY | os.O_DIRECTORY)
            try:
                os.fsync(dirfd)
            finally:
                os.close(dirfd)
        except OSError:
            pass

    _write_part_2(written, stderr=stderr)
    return written


