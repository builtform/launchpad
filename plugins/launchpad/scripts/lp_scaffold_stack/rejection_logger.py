"""scaffold-rejection-<ts>.jsonl writer (Layer 8/9 hardened inline protocol).

Per HANDSHAKE §4 final-paragraph + Layer 8 simplification + Layer 9
2-part-stderr surfacing. The forensic_writer.py SRP-split module is BL-223
deferred; v2.0 writes inline.

Protocol:

- Path: `.harness/observations/scaffold-rejection-<ISO 8601 microsec>.<pid>.jsonl`
  Microsec + pid suffix closes the same-second collision class.
- NO lock: `O_CREAT|O_EXCL|O_WRONLY` is the atomicity guarantee. On
  FileExistsError, retry with `.r2`/`.r3` suffix (max 5 retries; otherwise
  emit JSONL-fallback to stderr).
- File mode: 0o600 via `os.fchmod`.
- ENOENT/EROFS/EACCES/ENOSPC try/except fallback to stderr with
  `JSONL-fallback:` prefix — validation rejection MUST surface even on
  read-only / full filesystems.
- TWO-PART stderr surfacing:
    Part 1 (BEFORE write attempt): `reason:` + §4-rule-specific hint.
    Part 2 (AFTER `O_CREAT|O_EXCL` retry resolves): `log written to: <path>`
    OR `forensic log unavailable; reason captured above`.
  This ordering closes the L9 path-announcement-before-retry race where the
  announced path could differ from the final `.r2`/`.r3` path actually
  written.
- Single `os.write` ≤ 4096 bytes (PIPE_BUF).
- `os.fsync(fd)` + `os.fsync(dirfd)`; F_FULLFSYNC on darwin.
- ALWAYS-WRITTEN: NOT gated by `telemetry: off` — security/forensic events
  trump the analytics opt-out.
- schema_version: "1.0" (strict-policy per OPERATIONS §4 — readers reject
  absent OR unknown via single reason `scaffold_rejection_schema_version_invalid`).
"""
from __future__ import annotations

import errno
import fcntl
import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Mapping, TextIO

# Sibling-script imports.
_SCRIPTS = Path(__file__).resolve().parent.parent
if str(_SCRIPTS) not in sys.path:
    sys.path.insert(0, str(_SCRIPTS))

from pid_identity import get_pid_start_time  # noqa: E402

REJECTION_FILENAME_PREFIX = "scaffold-rejection-"
REJECTION_FILENAME_SUFFIX = ".jsonl"
MAX_LINE_BYTES = 4096
MAX_RETRIES = 5
SCHEMA_VERSION = "1.0"


def _harness_obs_dir(repo_root: Path) -> Path:
    return repo_root / ".harness" / "observations"


def _utc_now_microsec() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H-%M-%S.%fZ")


def _utc_now_iso_sec() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _build_payload(
    *,
    reason: str,
    field_name: str | None,
    seen_version: str | None,
    extra: Mapping[str, Any] | None,
) -> dict:
    payload: dict[str, Any] = {
        "schema_version": SCHEMA_VERSION,
        "reason": reason,
        "timestamp": _utc_now_iso_sec(),
        "pid": os.getpid(),
        "pid_start_time": get_pid_start_time(),
    }
    if field_name is not None:
        payload["field_name"] = field_name
    if seen_version is not None:
        payload["seen_version"] = seen_version
    if extra:
        for k, v in extra.items():
            if k not in payload:
                payload[k] = v
    return payload


